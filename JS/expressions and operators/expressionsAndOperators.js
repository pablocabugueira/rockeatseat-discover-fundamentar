// Expressões e operadores

let number = 1 // Expressão

/*
    Operators:
    - Binary
    - Unary
    - Ternary
*/

console.log(number + 1) // Binary

console.log(++number) // Unary
console.log(--number) // Unary

console.log(true ? "alo" : "oi") // Ternary
console.log(false ? "alo" : "oi") // Ternary