// Operadores de comparação
// Compara valores e retorna um boolean como resultado
// > Maior que
// >= Maior ou igual
// < Menor que
// <= Menor ou igual

let one = 1
let two = 2

console.log(one > 0) // False
console.log(one >= 1) // True

console.log(two < 3) // True
console.log(two <= 1) // False